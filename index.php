																									<h1>Зачем нужен сайт?</h1><p>Существует несколько основных типов сайтов и каждый из них выполняет те или иные потребности: <b>информация, имидж, поддержка клиентов, реклама, удаленная работа, хранение данных, развлечения, торговля</b> и т.д.
																									</p><p>Многие руководители компаний склонны размещать о себе информацию в различных каталогах сети интернет, в редких случаях этого достаточно, но собственный сайт – это очень удобный и многофункциональный бизнес-инструмент. Он может серьезно помочь в достижении сразу нескольких целей, каждая из которых очень важна для успеха и развития  бизнеса.
																									</p><p>Основная цель, которую преследуют все компании, занимающиеся любым видом бизнеса, одна - получение прибыли. Для достижения основной цели используются промежуточные, а именно увеличение узнаваемости фирмы, поиск партнеров, создание эффективной структуры и так далее. Для достижения этих целей разные компании используют различные бизнес-инструменты. И корпоративный веб-сайт является, пожалуй, самым универсальным из них. Он в той или иной степени способствует достижению пяти промежуточных целей.
																									</p><h2>Информация</h2><p>При наличии веб-сайта любой желающий, зная лишь название фирмы, может найти его в сети с помощью поисковой системы Яндекс или Google и узнать телефон, факс, адрес и другую интересующую информацию. Даже простой сайт-визитка способен привести к вам множество клиентов. Он работает 24 часа в сутки без обеда и выходных, удобный и надежный помощник в сети Интернет.
																									</p><h2>Создание имиджа</h2><p>Имидж успешной современной компании немыслим без веб-сайта в сети Интернет. И это должна быть не примитивная домашняя страничка на бесплатном хостинге. Подобное решение оказывает негативное влияние и отталкивает партнеров и потенциальных клиентов. Фирма, сайт которой размещен на бесплатном хостинге, кажется мелкой, вынужденной экономить каждую копейку. Причем совсем не обязательно делать потрясающую веб-страничку с флеш-анимацией или большой портал. Это может быть небольшой сайт с краткой информацией о фирме, ее услугах, контактной информацией и предложением о сотрудничестве. Но он должен иметь домен второго уровня и размещаться на платном сервере. 
																									</p><p>Еще одним важным "имиджевым" инструментом могут стать рекламные кампании, проведенные с привлечением сайта. Причем совсем не обязательно заниматься интернет-рекламой. Можно, например, устраивать различные маркетинговые акции вроде розыгрыша призов, а всю информацию об этом размещать на веб-странице. Особенно это важно для компаний, услуги или товары которых ориентированы на молодежь. 
																									</p><h2>Взаимодействие с потребителями</h2><p>Сайт - прекрасная возможность для двустороннего общения с потенциальными клиентами. И действительно, на веб-странице можно подробно рассказать о предлагаемых товарах и услугах, новых поступлениях и тому подобном. Согласитесь, сделать это с помощью других средств весьма затруднительно и довольно дорого. 
																									</p><p>Но это не главное. Гораздо важнее, что с помощью сайта можно получить отзывы потребителей о товарах или услугах. Согласитесь, что писать обычные письма или, тем более, звонить на фирму станут буквально единицы. Поэтому, для того чтобы узнать мнение потребителей, компаниям приходиться заказывать проведение специального исследования. Между тем, многие покупатели легко оставят свои отзывы на сайте фирмы - это не займет много времени и не потребует дополнительных затрат. Кроме того, веб-страницу можно использовать для проведения всевозможных маркетинговых исследований и опросов, которые необходимы для определения стратегии развития бизнеса. 
																									</p><h2>Взаимодействие с партнерами</h2><p>Успешное партнерство - обязательное условие успешного бизнеса. Поэтому поиск компаний, готовых сотрудничать с вашей - очень важное направление деятельности. И корпоративный сайт может не только увеличить эффективность этой работы, но и снизить ее стоимость. Особенно это утверждение верно в отношении потенциальных партнеров из других городов, а иногда и из разных стран. Действительно, вся информация о фирме и предлагаемом сотрудничестве расположена в Интернете, где она доступна из любой точки земного шара. Так что достаточно опять же через глобальную сеть найти компании, занимающиеся подходящей деятельностью, и посредством электронной почты связаться с их руководством. Причем довольно велика вероятность того, что ваш сайт тоже кто-то найдет и предложит взаимовыгодное сотрудничество. 
																									</p><p>Корпоративный сайт может облегчить не только поиск новых бизнес-партнеров, но и взаимодействие с уже существующими. Так, например, на нем может публиковаться информация, предназначенная для партнеров. Это более быстрый, удобный и дешевый способ, нежели отправление тех же данных по факсу или обычной почтой. А можно пойти еще дальше и организовать на базе корпоративного сайта систему обмена информацией. Например, для фирмы, занимающейся оптовыми продажами, это будет автоматическая генерация прайс-листов для дилеров на основе данных из бухгалтерских документов. 
																									</p><h2>Организация торговли</h2><p>На сегодняшний день в России открытие интернет-магазина "с нуля" вряд ли экономически оправдано. Совсем другое дело, когда это делает компания, уже занимающаяся торговлей. В этом случае первоначальные затраты минимальны. Ведь уже есть налаженные связи с поставщиками, офис, склад и прочее. Единственное, что нужно - это создать сам сайт и нанять дополнительный персонал. И тогда интернет-магазин способен принести прибыль - может быть, и не очень большую, но все же. Тем более что торговля в глобальной сети поможет увеличить товарооборот и узнаваемость компании среди потенциальных покупателей. Кстати, корпоративный сайт может способствовать увеличению клиентов и для организаций, занимающихся предоставлением различных услуг. Так, например, для кинотеатра это может быть предварительный заказ билетов, для парикмахерских и салонов красоты - запись к мастеру на определенное время и так далее. 
																									</p><h2>Взаимодействие внутри компании</h2><p>Когда у компании есть только один офис, все взаимодействие между сотрудниками и внутренний документооборот можно построить на базе локальной сети. Если у компании есть филиал, расположенный неподалеку от основного здания, то можно просто соединить обе сети между собой оптоволоконным каналом. Но что делать, если фирма довольно крупная и имеет несколько офисов, расположенных по всему городу, а то и разбросанных по разным регионами? Как эффективно организовать взаимодействие между структурными подразделениями? Ответ может быть только один - с помощью Интернета. Точнее, с помощью корпоративного сайта, на котором под соответствующей защитой будет находиться нужная информация. Ну и, естественно, с помощью веб-сервера можно реализовать различные распределенные информационные системы. 
																									</p><h2>Реклама</h2><p>Последнее время все большее количество компаний вкладывают деньги в интернет-рекламу и это неудивительно, ведь это самая дешевая реклама, которая позволяет охватить огромную аудиторию и получить максимальную отдачу. Оговорюсь, что реклама в Интернете выгодна не всем компаниям, это надо понимать и предварительно проконсультироваться в агенстве интернет-рекламы. Интернет позволяет донести до большого числа людей информацию (рекламу) о товарах и услугах компании, и это огромный плюс.
